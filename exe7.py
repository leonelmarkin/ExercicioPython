#Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.

ladoA = float(input('Insira a medida de cima: '))

ladoB = float(input('Insira a medida do lado: '))

caluculaQuadrado = ladoA + ladoB * 2

dobroQuadrado = caluculaQuadrado * 2

print(f'Aqui está o dobro desta area: {dobroQuadrado}')

